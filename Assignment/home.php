    	What do you know about Mumbai University?
    </div>
    
    <div id="space"></div>
    
    <div style="text-indent:40px;text-align:justify;text-justify: inter-word;font-family:arial">
   The profile of this University carved out in 156 years of its functioning attests to its manifold achievements as the intellectual and moral powerhouse of the society. The University has always given its best to the country in general and to the city of Mumbai in particular by enthusiastically shouldering an ever-growing load of social values and opportunities.

Initially, the University concentrated its efforts on controlling teaching at the undergraduate level and in conducting examinations. Later on it took up research and the task of imparting instructions at the Post-Graduate level. This resulted in the establishment of the University Departments beginning with the School of Sociology and Civics & Politics. The independence of the country led to the re-organization of the functions and powers of the University with the passing of the Bombay University Act of 1953.

It has two campuses of areas 243 acres and 14 acres at Vidyanagari and Fort respectively; sub-campuses/centers at Ratnagiri 20 acres, Thane 6.50 acres and Kalyan 6.26 acres with 60 University Departments & Institutes and 749 affiliated colleges. It has established its name in industrial & International collaborations and runs various professional courses.

At national level, it has excelled in sports, cultural and out-reach activities. In the last five years it has seen 104% increase in under-graduate students, 112% increase in post-graduate students and 147% increase in distance - education students. There is 156% increase in the number of research papers published in International journals. Twelve Department/sections are recognized under various national programmes, such as SAP/CAS/DRS/DSA/COSIST/FIST. More than eighty teachers are on various professional bodies. Eighteen National/International awards are won by teachers in the last five years. Every year about 20 teachers visit abroad for academic activities. Recently more than ten self-supporting courses have been started by the University


